//向SPI总线传输一个8位数据
void SendByte(unsigned char Data)
{
  unsigned char i=0;
  
  for(i=8;i>0;i--)
  {
    if(Data&0x80)	
      LCD_SDA_SET;//输出数据
    else 
      LCD_SDA_CLR;
    
    LCD_SCL_CLR;       
    LCD_SCL_SET;
    Data<<=1; 
  }
}

//向SPI总线传输一个8位数据
void  SPI_WriteData(unsigned char Data)
{
  unsigned char i=0;
  
  for(i=8;i>0;i--)
  {
    if(Data&0x80)	
      LCD_SDA_SET;//输出数据
    else 
      LCD_SDA_CLR;
    
    LCD_SCL_CLR;       
    LCD_SCL_SET;
    Data<<=1; 
  }
}

//向液晶屏写一个8位指令
void LCD_WrCmd(unsigned char Index)
{ 
  //LCD_CS_CLR;  //ALD del  SPI写命令时序开始
  LCD_DC_CLR;
  SPI_WriteData(Index);
  //LCD_CS_SET;  //ALD del
}

//向液晶屏写一个8位数据
void LCD_WrDat(unsigned char Data)
{
  //LCD_CS_CLR;  //ALD del
  LCD_DC_SET;
  SPI_WriteData(Data);
  //LCD_CS_SET;  //ALD del 
}

//向液晶屏写一个16位数据
void LCD_WriteData_16Bit(unsigned short Data)
{
  //LCD_CS_CLR;  //ALD del
  LCD_DC_SET;
  SPI_WriteData(Data>>8); //写入高8位数据
  SPI_WriteData(Data); 	  //写入低8位数据
  //LCD_CS_SET;  //ALD del 
}

